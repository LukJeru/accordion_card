const drop1 = document.getElementById("dropdown_1");

const func1 = (event) => {
  let para  = document.getElementById("paragraph_1");
  if (para.style.display === "none") {
    para.style.display = "block"
  } else {
    para.style.display = "none";
  };
}

drop1.addEventListener("click", func1)

